package com.basics;

public class UnaryOper1 {

	public static void main(String[] args) {
		int x = 10, a=10, b=10;
		System.out.println(x++);
		System.out.println(x);
		System.out.println(++x);
		System.out.println(x);
		System.out.println(--x);
		System.out.println(x);
		System.out.println(x--);
		System.out.println(x);
		System.out.println(a++ + ++a);
	}

}
